//////////////////////////////////////////////////////////////////////

BOOL CStatusBar::Create(CWindow *pParent)
{
	BOOL bResult = (BOOL)CWindow::Create(STATUSCLASSNAME,
		pParent->m_hWnd,
		NULL, 
		NULL, 
		WS_CHILD | WS_VISIBLE);

	if (bResult)
		SendMessage(SB_SIMPLE, TRUE);

	return bResult;
}

//////////////////////////////////////////////////////////////////////

void CStatusBar::SetSimpleText(LPCTSTR lpText)
{
	SendMessage(SB_SETTEXT, SB_SIMPLEID, (LPARAM)lpText);	
}

